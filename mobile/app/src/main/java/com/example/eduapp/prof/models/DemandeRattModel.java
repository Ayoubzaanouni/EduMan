package com.example.eduapp.prof.models;

public class DemandeRattModel {
    private String demandeType;
    private String demandeDateRatt;
    private String demandeHeureDebut;
    private String demandeHeureFin;
    private String demandeMatiere;
    private String demandeClasse;

    public String getDemandeType() {
        return demandeType;
    }

    public void setDemandeType(String demandeType) {
        this.demandeType = demandeType;
    }

    public String getDemandeDateRatt() {
        return demandeDateRatt;
    }

    public void setDemandeDateRatt(String demandeDateRatt) {
        this.demandeDateRatt = demandeDateRatt;
    }

    public String getDemandeHeureDebut() {
        return demandeHeureDebut;
    }

    public void setDemandeHeureDebut(String demandeHeureDebut) {
        this.demandeHeureDebut = demandeHeureDebut;
    }

    public String getDemandeHeureFin() {
        return demandeHeureFin;
    }

    public void setDemandeHeureFin(String demandeHeureFin) {
        this.demandeHeureFin = demandeHeureFin;
    }

    public String getDemandeMatiere() {
        return demandeMatiere;
    }

    public void setDemandeMatiere(String demandeMatiere) {
        this.demandeMatiere = demandeMatiere;
    }

    public String getDemandeClasse() {
        return demandeClasse;
    }

    public void setDemandeClasse(String demandeClasse) {
        this.demandeClasse = demandeClasse;
    }
// Constructor, getters, and setters
}
