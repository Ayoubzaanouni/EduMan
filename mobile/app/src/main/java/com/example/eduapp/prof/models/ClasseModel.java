package com.example.eduapp.prof.models;

public class ClasseModel {
    private int assClasseId;
    private String assClasseName;
    private int assId;

    public ClasseModel() {
    }

    public int getAssClasseId() {
        return assClasseId;
    }

    public void setAssClasseId(int assClasseId) {
        this.assClasseId = assClasseId;
    }

    public String getAssClasseName() {
        return assClasseName;
    }

    public void setAssClasseName(String assClasseName) {
        this.assClasseName = assClasseName;
    }

    public int getAssId() {
        return assId;
    }

    public void setAssId(int assId) {
        this.assId = assId;
    }
}
