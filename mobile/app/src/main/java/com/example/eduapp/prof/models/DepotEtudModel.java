package com.example.eduapp.prof.models;

public class DepotEtudModel {

    String depotEtudiantNom;
    String depotEtudiantClasse;
    String depotEtudiantFile;
    String depotEtudiantTime;

    public DepotEtudModel() {
    }

    public String getDepotEtudiantNom() {
        return depotEtudiantNom;
    }

    public void setDepotEtudiantNom(String depotEtudiantNom) {
        this.depotEtudiantNom = depotEtudiantNom;
    }

    public String getDepotEtudiantClasse() {
        return depotEtudiantClasse;
    }

    public void setDepotEtudiantClasse(String depotEtudiantClasse) {
        this.depotEtudiantClasse = depotEtudiantClasse;
    }

    public String getDepotEtudiantFile() {
        return depotEtudiantFile;
    }

    public void setDepotEtudiantFile(String depotEtudiantFile) {
        this.depotEtudiantFile = depotEtudiantFile;
    }

    public String getDepotEtudiantTime() {
        return depotEtudiantTime;
    }

    public void setDepotEtudiantTime(String depotEtudiantTime) {
        this.depotEtudiantTime = depotEtudiantTime;
    }
}
